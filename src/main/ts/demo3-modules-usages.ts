/// <reference path="./demo3-modules-declarations.ts" />
////////////////////////////////////////////////////////////////////////////////////////////////////
// packages are open-ended - can divide classes/interfaces of the same module into separate files
////////////////////////////////////////////////////////////////////////////////////////////////////
module ee.iglu.demo {
	////////////////////////////////////////////////////////////////////////////////////////////////////
	// interfaces are open-ended - plugins can add members/methods to existing objects/classes
	////////////////////////////////////////////////////////////////////////////////////////////////////
	export interface PublicType {
		extraField?: string;
		extraFunction?(): string;
	}
}



////////////////////////////////////////////////////////////////////////////////////////////////////
// variables and classes exported from modules are made visible to code outside those modules
////////////////////////////////////////////////////////////////////////////////////////////////////
console.log(ee.iglu.demo.publicVar);
var obj1: ee.iglu.demo.PublicType = new ee.iglu.demo.PublicClass("");
obj1.extraFunction();



////////////////////////////////////////////////////////////////////////////////////////////////////
// package names can be aliased using variable assignment
////////////////////////////////////////////////////////////////////////////////////////////////////
var demo = ee.iglu.demo;
var obj2 = new demo.PublicClass("");

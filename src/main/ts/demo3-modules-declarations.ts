module ee.iglu.demo {
	////////////////////////////////////////////////////////////////////////////////////////////////////
	// variables and classes without "export" modifier are not made visible outside the module
	////////////////////////////////////////////////////////////////////////////////////////////////////
	var privateVar = "Not visible to outside";
	class PrivateClass{
		constructor() {
			alert("privateVar: "+ privateVar);
		}
	}


	////////////////////////////////////////////////////////////////////////////////////////////////////
	// exported variables and classes inside this module are made visible to code outside this module
	////////////////////////////////////////////////////////////////////////////////////////////////////
	export var publicVar = "Visible to outside";
	export interface PublicType {
		someField: string;
	}
	export class PublicClass implements PublicType {
		constructor(public someField: string) {
			alert("privateVar: "+ privateVar);
		}
	}


	////////////////////////////////////////////////////////////////////////////////////////////////////
	// modules can be defined inside this module,
	// they can see private members
	////////////////////////////////////////////////////////////////////////////////////////////////////
	export module x { // module name is ee.iglu.demo.x
		alert(privateVar);
	}
}
